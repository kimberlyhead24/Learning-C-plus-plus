#include <iostream>
/* Program that says 'Hello, World' in 
   3 languages: English, French, and Spanish */
int main() {
    std::cout << "Hello, World (English)\n"; // English translation
    std::cout << "Bonjour, le monde! (French)\n"; // French translation
    std::cout << "Hola, Mundo! (Spanish)\n";  // Spanish translation
    
    return 0;
}
